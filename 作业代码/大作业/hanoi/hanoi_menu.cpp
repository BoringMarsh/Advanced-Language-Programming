/***************************************************************************
  函数名称：menu
  功    能：显示各菜单项，读入正确的选项后返回
  输入参数：无
  返 回 值：菜单项对应的编号
  说    明：1、本函数为27行
            2、已按要求控制读入正确选项
***************************************************************************/
int menu()
{
	int ret;
	cout << "---------------------------------\n"
		<< "1.基本解\n"
		<< "2.基本解(步数记录)\n"
		<< "3.内部数组显示(横向)\n"
		<< "4.内部数组显示(纵向+横向)\n"
		<< "5.图形解-预备-画三个圆柱\n"
		<< "6.图形解-预备-在起始柱上画n个盘子\n"
		<< "7.图形解-预备-第一次移动\n"
		<< "8.图形解-自动移动版本\n"
		<< "9.图形解-游戏版\n"
		<< "0.退出\n"
		<< "---------------------------------\n"
		<< "[请选择:] ";

	while (1) {
		ret = _getch();

		if (ret >= '0' && ret <= '9') {
			break;
		}
	}

	return ret - '0';
}