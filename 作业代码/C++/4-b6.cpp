	int n;
	cout << "计算legendre，请输入x和n的值" << endl;     /*输出提示语*/
	cin >> x >> n;                                      /*输入x、n*/
	cout << "legendre[" << n << "](" << x << ")=" << legendre(x, n) << endl;     /*按格式输出结果*/
